#print a number
print(1)

#print a decimal
print(1.0)

#print a negative number
print(-1)

#print arithmatics
print(1+1)

#print order of operation
print((2+2)*5)

#Print the remainder, or "mod(%)"
print(10%3)

#print numbers along side strings
my_num = 5
print(str(my_num) + " my favorite number")

#functions
#absolute value
print(abs(my_num))

#power (x^y)
print(pow(2, 2))

#max function
#determine the biggest number
print(max(4, 5, 6))

#round function
print(round(4.8))

#import python math
